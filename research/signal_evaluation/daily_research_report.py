def _section_alpha_decay(day_df: pd.DataFrame) -> list[str]:
    dir_rows = _directional_rows(day_df)

    lines = [
        "## 6. Signal Alpha Decay Curve",
        "",
        "*Alpha decay measures how the signal's directional edge evolves from generation through "
        "session close. In efficient markets, information is gradually incorporated into price — "
        "the speed of this incorporation determines the signal's information half-life (Kyle, 1985). "
        "A steep decay curve suggests the market quickly absorbs the signal's information content. "
        "The ASCII visualization below shows the decay trajectory across all evaluation horizons.*",
        "",
        "Average directional-signed return (bps) by time horizon:",
        "",
        "| Horizon | Avg Signed Return (bps) |",
        "| --- | ---: |",
    ]

    decay_data: list[tuple[str, float | None]] = []
    for label, _, signed_col, _ in HORIZON_DEFS:
        s = _numeric(dir_rows.get(signed_col, pd.Series()))
        mean_val = s.mean() if s.notna().any() else None
        decay_data.append((label, mean_val))
        lines.append(f"| {label} | {_r(mean_val, 2)} |")

    lines.append("")

    # ASCII decay chart
    lines.append("**Decay Curve (ASCII):**")
    lines.append("")
    lines.append("```")
    max_abs = max((abs(v) for _, v in decay_data if v is not None and pd.notna(v)), default=1.0)
    bar_width = 40
    for label, val in decay_data:
        if val is None or pd.isna(val):
            lines.append(f"  {label:>15s} | {'?' * 3}")
        else:
            norm = val / max_abs if max_abs > 0 else 0
            bar_len = int(abs(norm) * bar_width)
            char = "+" if val >= 0 else "-"
            pad = " " * (bar_width - bar_len) if val >= 0 else ""
            bar = char * max(bar_len, 1)
            lines.append(f"  {label:>15s} | {pad}{bar} {val:+.1f} bps")
    lines.append("```")
    lines.append("")
    lines.append(
        "The decay curve reveals how long the signal edge persists after generation. "
        "Steeper decay suggests shorter-lived alpha."
    )
    lines.append("")

    return lines


