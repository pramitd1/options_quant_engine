def _section_reversal_diagnostics(day_df: pd.DataFrame) -> list[str]:
    dir_rows = _directional_rows(day_df)

    lines = [
        "## 14. Reversal Diagnostics",
        "",
        "*Reversal diagnostics measure how much profit is surrendered after peak favorable excursion "
        "(MFE). The peak-to-close decay reveals the magnitude of late-session profit erosion, while "
        "the sign-flip rate measures how often initially profitable signals reverse direction. High "
        "reversal rates suggest the alpha is transient — profitable at intermediate horizons but "
        "competed away by session close. This informs exit timing and holding period optimization.*",
        "",
        "Intraday reversal and profit decay analysis:",
        "",
    ]

    mfe_60 = _numeric(dir_rows.get("mfe_60m_bps", pd.Series()))
    signed_close = _numeric(dir_rows.get("signed_return_session_close_bps", pd.Series()))
    signed_60 = _numeric(dir_rows.get("signed_return_60m_bps", pd.Series()))

    lines.append("| Metric | Value |")
    lines.append("| --- | ---: |")

    # Peak-to-close decay: MFE(60m) - session_close return
    if mfe_60.notna().any() and signed_close.notna().any():
        peak_to_close = (mfe_60 - signed_close).mean()
        lines.append(f"| Avg Peak-to-Close Decay (bps) | {_r(peak_to_close, 2)} |")

