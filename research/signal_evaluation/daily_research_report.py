def _section_executive_summary(df: pd.DataFrame, day_df: pd.DataFrame, report_date: date) -> list[str]:
    total = len(day_df)
    directional = len(_directional_rows(day_df))
    trade_signals = int((day_df.get("trade_status", pd.Series()) == "TRADE").sum())
    dir_rows = _directional_rows(day_df)

    # Best horizon hit rate
    best_horizon = "—"
    best_hr = 0.0
    for label, _, _, correct_col in HORIZON_DEFS:
        if correct_col in dir_rows.columns:
            hr = _numeric(dir_rows[correct_col]).mean()
            if pd.notna(hr) and hr > best_hr:
                best_hr = hr
                best_horizon = label

    # Composite score stats
    cs = _numeric(day_df.get("composite_signal_score", pd.Series()))
    mean_cs = _r(cs.mean(), 1)

    macro = day_df["macro_regime"].mode().iloc[0] if "macro_regime" in day_df.columns and not day_df["macro_regime"].mode().empty else "—"
    gamma = day_df["gamma_regime"].mode().iloc[0] if "gamma_regime" in day_df.columns and not day_df["gamma_regime"].mode().empty else "—"

    lines = [
        "## 1. Executive Summary",
        "",
        "*This section provides a high-level overview of session signal activity, dominant regimes, "
        "and the best-performing evaluation horizon. The executive summary synthesizes the engine's "
        "output into key performance indicators that characterize the day's signal quality. "
        "A strong session shows high composite scores, above-50% hit rates at multiple horizons, "
        "and consistent directional edge across evaluation windows.*",
        "",
        f"**Date:** {report_date.strftime('%B %d, %Y')}",
        "",
        f"The engine generated **{total} signal snapshots** during the session, "
        f"of which **{directional}** carried a directional bias and **{trade_signals}** "
        f"qualified as actionable trade signals.",
        "",
        f"- **Mean composite signal score:** {mean_cs}",
        f"- **Best horizon hit rate:** {best_horizon} ({_pct(best_hr)})" if best_hr > 0 else "- **Best horizon hit rate:** insufficient data",
        f"- **Dominant macro regime:** {macro}",
        f"- **Dominant gamma regime:** {gamma}",
        "",
    ]
    return lines


