def _append_summary(lines: list[str], section_title: str, section_lines: list[str],
                    fallback_summary: str, narrative: bool) -> None:
    """Append a data-driven summary to the section.

    The rule-based summary is always emitted as the authoritative source of
    truth because it is computed directly from the report data.  When
    *narrative* is True and an LLM is reachable, append an additional
    interpretation paragraph that is explicitly grounded to the computed
    summary and the section tables.
    """
    if fallback_summary:
        lines.append(f"> **Summary:** {fallback_summary}")
        lines.append("")

    if narrative:
        theory = SECTION_THEORY_CONTEXT.get(section_title)
        structured_payload = _build_structured_section_payload(
            section_title,
            section_lines,
            fallback_summary,
        )
        ai_text = _ai_narrative(
            section_title,
            theory_context=theory,
            ground_truth_summary=fallback_summary,
            structured_metrics=structured_payload,
        )
        if ai_text:
            if _interpretation_conflicts_with_kpis(
                section_title,
                ai_text,
                structured_payload,
                fallback_summary,
            ):
                logger.warning(
                    "Suppressed AI interpretation for section '%s' due to KPI consistency conflict.",
                    section_title,
                )
            else:
                lines.append(f"> **Interpretation:** {ai_text}")
                lines.append("")


def generate_daily_report(
    report_date: date | None = None,
    dataset_path: str | Path | None = None,
    output_dir: str | Path | None = None,
    narrative: bool = False,
    mode: str = "daily",
    run_evaluation: bool = True,
) -> Path:
    """Generate a signal research report.

    Parameters
    ----------
    report_date : date, optional
        The trading day to report on. Defaults to the most recent day in the dataset.
    dataset_path : str or Path, optional
        Path to signals_dataset.csv.
    output_dir : str or Path, optional
        Directory for report output.
    narrative : bool
        If True, enrich each section with AI-generated commentary via OpenAI API.
        Requires OPENAI_API_KEY in the environment. Defaults to False.
    mode : str
        ``"daily"`` — generate a day-specific report analysing only signals for
        *report_date*.  A new file is created for each trading day.

        ``"cumulative"`` — generate a cumulative metrics report analysing the
        **entire** signals dataset.  The file is overwritten on each run,
        providing an always-up-to-date cumulative view.

    Returns
    -------
    Path
        The path to the generated markdown report.
    """
    if mode not in ("daily", "cumulative"):
        raise ValueError(f"mode must be 'daily' or 'cumulative', got {mode!r}")

    # Always load the cumulative dataset for historical context when no explicit path
    # is given. Daily mode filters analysis_df down to today; cumulative uses all rows.
    if dataset_path:
        ds_path = Path(dataset_path)
    elif DEFAULT_CUMULATIVE_DATASET_PATH.exists():
        ds_path = DEFAULT_CUMULATIVE_DATASET_PATH
    else:
        ds_path = DEFAULT_DATASET_PATH

    # Backfill realized outcomes for any PENDING signals before building the report.
    if run_evaluation and ds_path.exists():
        logger.info("Evaluating signal outcomes in %s before report generation ...", ds_path.name)
        update_signal_dataset_outcomes(dataset_path=ds_path)
        logger.info("Signal outcome evaluation complete.")

    out_dir = Path(output_dir) if output_dir else DEFAULT_OUTPUT_DIR
    out_dir.mkdir(parents=True, exist_ok=True)

    df = _load_dataset(ds_path)
    if df.empty:
        raise ValueError(f"Dataset is empty: {ds_path}")

    # Resolve report date
    if report_date is None:
        latest = df["signal_timestamp"].max()
        report_date = latest.date() if pd.notna(latest) else date.today()

    day_df = _filter_day(df, report_date)
    if mode == "daily" and day_df.empty:
        logger.warning("No signals found for %s — generating report with empty data.", report_date)

    # In cumulative mode the analysis data is the full dataset.
    # In daily mode, enforce strict day-only scope across *all* sections.
    is_cumulative = mode == "cumulative"
    analysis_df = df if is_cumulative else day_df
    scope_df = df if is_cumulative else analysis_df

    # Determine date range for cumulative reports
    if is_cumulative and "signal_timestamp" in df.columns:
        first_date = df["signal_timestamp"].min().date()
        last_date = df["signal_timestamp"].max().date()
        n_days = df["signal_timestamp"].dt.date.nunique()
        date_range_str = f"{first_date.strftime('%Y-%m-%d')} to {last_date.strftime('%Y-%m-%d')}"
    else:
        first_date = last_date = report_date
        n_days = 1
        date_range_str = report_date.strftime('%Y-%m-%d')

    # Titles and labels
    if is_cumulative:
        report_title = f"Cumulative Signal Research Report — through {last_date.strftime('%B %d, %Y')}"
        report_subtitle = "Cumulative Signal Quality and Predictive Performance Analysis"
        scope_note = (
            f"*This report evaluates cumulative signal quality across **{n_days} trading day(s)** "
            f"({date_range_str}), covering **{len(df)} total signals**. "
            "It is regenerated on each run with the latest dataset.*"
        )
    else:
        report_title = f"Daily Signal Research Report — {report_date.strftime('%B %d, %Y')}"
        report_subtitle = "Signal Quality and Predictive Performance Analysis"
        scope_note = (
            "*This report evaluates signal quality and predictive power only. "
            "It does not depend on or reference trade execution, fills, slippage, or capital allocation.*"
        )

    # Build report markdown
    lines: list[str] = []

    # Header
    lines.extend([
        "---",
        f"title: \"{report_title}\"",
        f"subtitle: \"{report_subtitle}\"",
        "author: \"Pramit Dutta — Quant Engines\"",
        f"date: \"{report_date.strftime('%Y-%m-%d')}\"",
        "---",
        "",
        f"# {report_title}",
        "",
        f"> **Author:** Pramit Dutta | **Organization:** Quant Engines | "
        f"**Date:** {report_date.strftime('%Y-%m-%d')}"
        + (f" | **Period:** {date_range_str}" if is_cumulative else ""),
        "",
        "---",
        "",
        "### Signal Terminology",
        "",
        "| Term | Definition |",
        "| --- | --- |",
        "| **Signal Snapshot** | A single evaluation point where the engine assessed market conditions |",
        "| **Directional Signal** | A snapshot where the model predicted a direction (CALL or PUT) |",
        "| **Trade-Qualified Signal** | A directional signal that passed composite score and filter thresholds |",
        "| **Neutral / No-Signal** | A snapshot where conditions did not warrant a directional prediction |",
        "",
        scope_note,
        "",
        "---",
        "",
    ])

    # All sections — each with a data-driven summary (rule-based fallback
    # always present; LLM-generated when --narrative is used and available).
    # Sections that display a "Full Dataset | Today" dual column always receive
    # the real full df and the real day_df so that the two columns are distinct
    # in both daily and cumulative modes.
    _sections = [
        ("Executive Summary",
         _section_executive_summary(scope_df, analysis_df, report_date),
         _summarize_executive(scope_df, analysis_df, report_date)),
        ("Signal Dataset Summary",
         _section_dataset_summary(df, day_df),
         _summarize_dataset_summary(df, day_df)),
        ("Macroeconomic Environment",
         _section_macro_environment(analysis_df),
         _summarize_macro(analysis_df)),
        ("Market Structure Context",
         _section_market_structure(analysis_df),
         _summarize_market_structure(analysis_df)),
        ("Signal Generation Summary",
         _section_signal_generation(analysis_df),
         _summarize_signal_generation(analysis_df)),
        ("Gate Suppression Diagnostics",
         _section_gate_suppression_diagnostics(analysis_df),
         _summarize_gate_suppression(analysis_df)),
        ("Horizon Performance",
         _section_horizon_performance(analysis_df),
         _summarize_horizon_performance(analysis_df)),
        ("Signal Alpha Decay Curve",
         _section_alpha_decay(analysis_df),
         _summarize_alpha_decay(analysis_df)),
        ("Decay Curve by Score Bucket",
         _section_decay_by_score_bucket(analysis_df),
         _summarize_decay_by_score(analysis_df)),
        ("Decay Curve by Regime",
         _section_decay_by_regime(analysis_df),
         _summarize_decay_by_regime(analysis_df)),
        ("Directional Accuracy",
         _section_directional_accuracy(analysis_df),
         _summarize_directional_accuracy(analysis_df)),
        ("Magnitude Adequacy",
         _section_magnitude_adequacy(analysis_df),
         _summarize_magnitude(analysis_df)),
        ("Tradeability Analysis",
         _section_tradeability(analysis_df),
         _summarize_tradeability(analysis_df)),
        ("Premium Analytics",
         _section_premium_analytics(analysis_df),
         _summarize_premium_analytics(analysis_df)),
        ("Score Calibration",
         _section_score_calibration(analysis_df),
         _summarize_score_calibration(analysis_df)),
        ("Probability Calibration",
         _section_probability_calibration(analysis_df),
         _summarize_probability_calibration(analysis_df)),
        ("Reversal Diagnostics",
         _section_reversal_diagnostics(analysis_df),
         _summarize_reversal(analysis_df)),
        ("Regime Performance",
         _section_regime_performance(analysis_df),
         _summarize_regime_performance(analysis_df)),
        ("Information Coefficient",
         _section_information_coefficient(df, day_df),
         _summarize_information_coefficient(df, day_df)),
        ("Signal Edge Distribution",
         _section_edge_distribution(analysis_df),
         _summarize_edge_distribution(analysis_df)),
        ("Signal Stability Metrics",
         _section_signal_stability(df, day_df),
         _summarize_signal_stability(df, day_df)),
        ("Exit Horizon Diagnostic",
         _section_exit_horizon(analysis_df),
         _summarize_exit_horizon(analysis_df)),
        ("Regime Coverage Tracker",
         _section_regime_coverage(df),
         _summarize_regime_coverage(df)),
        ("Feature Variance Check",
         _section_feature_variance(analysis_df),
         _summarize_feature_variance(analysis_df)),
        ("Model Diagnostics",
         _section_model_diagnostics(analysis_df),
         _summarize_model_diagnostics(analysis_df)),
        ("Key Insights",
         _section_key_insights(analysis_df, report_date),
         _summarize_key_insights(analysis_df)),
        ("Research Actions",
         _section_research_actions(analysis_df),
         _summarize_research_actions(analysis_df)),
    ]

    for idx, (section_title, section_lines, fallback_summary) in enumerate(_sections, 1):
        # Auto-number sections: replace any existing ## heading with correct number
        if section_lines and section_lines[0].startswith("## "):
            section_lines = [f"## {idx}. {section_title}"] + section_lines[1:]
        lines.extend(section_lines)
        _append_summary(lines, section_title, section_lines, fallback_summary, narrative)

    # Footer
    lines.extend([
        "---",
        "",
        f"*Report generated by Options Quant Engine — Signal Research Reporting System*",
        "",
        f"*Dataset: {ds_path.name} | Signals analysed: {len(analysis_df)} | "
        f"Mode: {mode} | Date: {report_date.strftime('%Y-%m-%d')}*",
    ])

    report_text = "\n".join(lines)
    if is_cumulative:
        filename = "signal_research_report_cumulative.md"
    else:
        filename = f"signal_research_report_{report_date.strftime('%Y%m%d')}.md"
    report_path = out_dir / filename
    report_path.write_text(report_text, encoding="utf-8")

    logger.info("%s research report written to %s", mode.capitalize(), report_path)
    return report_path
