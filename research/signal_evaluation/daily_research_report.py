def _section_market_structure(day_df: pd.DataFrame) -> list[str]:
    lines = [
        "## 3. Market Structure Context",
        "",
        "*Market microstructure shapes how price responds to order flow. Gamma regime determines "
        "whether dealer hedging amplifies (negative gamma) or dampens (positive gamma) directional "
        "moves. Dealer positioning, liquidity structure, and squeeze risk state together define "
        "the path environment in which signals must operate. Favorable microstructure increases "
        "the probability that directionally correct signals translate into profitable paths.*",
        "",
        "| Dimension | Observed State |",
        "| --- | --- |",
    ]

    structure_fields = [
        ("gamma_regime", "Gamma Regime"),
        ("dealer_position", "Dealer Positioning"),
        ("spot_vs_flip", "Spot vs Gamma Flip"),
        ("volatility_regime", "Volatility Regime"),
        ("liquidity_vacuum_state", "Liquidity Structure"),
        ("dealer_hedging_bias", "Dealer Hedging Bias"),
        ("squeeze_risk_state", "Squeeze Risk State"),
        ("directional_convexity_state", "Directional Convexity"),
        ("dealer_flow_state", "Dealer Flow State"),
    ]

    for col, label in structure_fields:
        if col in day_df.columns:
            vals = day_df[col].dropna()
            if not vals.empty:
                mode = vals.mode().iloc[0] if not vals.mode().empty else "—"
                unique = vals.nunique()
                if unique > 1:
                    lines.append(f"| {label} | {mode} (dominant, {unique} states observed) |")
                else:
                    lines.append(f"| {label} | {mode} |")

    # Support / resistance from spot data (exclude non-positive values)
    if "day_high" in day_df.columns and "day_low" in day_df.columns:
        highs = _positive_numeric(day_df["day_high"])
        lows = _positive_numeric(day_df["day_low"])
        high = highs.max() if highs.notna().any() else None
        low = lows.min() if lows.notna().any() else None
        if pd.notna(high) and pd.notna(low):
            lines.append(f"| Session High | {_r(high, 2)} |")
            lines.append(f"| Session Low | {_r(low, 2)} |")
            lines.append(f"| Session Range | {_r(high - low, 2)} pts |")
        else:
            lines.append("| Session Range | — (insufficient valid spot extremes) |")

    lines.append("")
    return lines


def _section_signal_generation(day_df: pd.DataFrame) -> list[str]:
    total = len(day_df)
    dir_rows = _directional_rows(day_df)
    directional = len(dir_rows)
    neutral = total - directional
    status = day_df.get("trade_status", pd.Series(index=day_df.index, dtype=str)).astype(str).str.upper()
    trade = int((status == "TRADE").sum())
    # Keep this bucket aligned with its label by counting all non-trade snapshots.
    watchlist = int((status != "TRADE").sum())

    lines = [
        "## 4. Signal Generation Summary",
        "",
        "*The signal generation pipeline produces snapshots at regular intervals, each evaluated for "
        "directional bias and trade qualification. Directional signals carry a CALL or PUT bias; "
        "trade-qualified signals pass additional filter criteria (score thresholds, regime alignment, "
        "volatility conditions). The ratio of trade-qualified to total signals reflects the engine's "
        "selectivity — higher selectivity typically correlates with better per-signal quality.*",
        "",
        "| Metric | Count |",
        "| --- | ---: |",
        f"| Total signal snapshots | {total} |",
        f"| Directional signals | {directional} |",
        f"| Neutral / no-direction | {neutral} |",
        f"| Qualified trade signals | {trade} |",
        f"| Watchlist / no-signal | {watchlist} |",
        "",
    ]

    # Direction breakdown
    if "direction" in day_df.columns:
        dir_counts = day_df["direction"].value_counts()
        if not dir_counts.empty:
            lines.append("**Direction breakdown:**")
            lines.append("")
            for d, c in dir_counts.items():
                lines.append(f"- {d}: {c}")
            lines.append("")

            directional_total = int(dir_counts.sum())
            if directional_total > 0:
                dominant_direction = str(dir_counts.index[0])
                dominant_count = int(dir_counts.iloc[0])
                dominant_share = dominant_count / directional_total
                lines.append("**Directional concentration diagnostic:**")
                lines.append("")
                lines.append("| Metric | Value |")
                lines.append("| --- | ---: |")
