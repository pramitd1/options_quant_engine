def _summarize_score_calibration(day_df: pd.DataFrame) -> str:
    dir_rows = _directional_rows(day_df)
    if dir_rows.empty or "composite_signal_score" not in dir_rows.columns:
        return "Insufficient data for score calibration analysis."
    working = dir_rows.copy()
    working["composite_signal_score"] = _numeric(working["composite_signal_score"])
    bucket_hrs = []
    for lo, hi, label in SCORE_BUCKETS:
        mask = (working["composite_signal_score"] >= lo) & (working["composite_signal_score"] <= hi)
        bucket = working.loc[mask]
        if not bucket.empty and "correct_60m" in bucket.columns:
            hr = _numeric(bucket["correct_60m"]).mean()
            if pd.notna(hr):
                bucket_hrs.append((label, hr, len(bucket)))
    if len(bucket_hrs) < 2:
        return "Too few score buckets with outcome data — calibration requires more observations."
    hrs_only = [h for _, h, _ in bucket_hrs]
    monotonic = all(hrs_only[i] >= hrs_only[i + 1] for i in range(len(hrs_only) - 1))
    summary = f"{bucket_hrs[0][0]} bucket hit {_pct(bucket_hrs[0][1])} at 60m (N={bucket_hrs[0][2]}). "
    cal_note = (
        "Monotonic hit-rate ordering across score buckets confirms the scoring model correctly "
        "ranks signal quality. Higher composite scores map to better realized outcomes, meaning "
        "the feature weights in the scoring function capture genuinely informative dimensions. "
        "This calibration supports using the composite score for position sizing and signal filtering."
        if monotonic
        else "Non-monotonic ordering detected — lower-scored signals outperformed higher-scored ones "
        "at certain tiers. This calibration failure suggests the composite score's feature weights "
        "do not accurately reflect each feature's contribution to predictive power. Feature "
        "importance analysis and weight adjustment are recommended to restore monotonic ordering."
    )
    return f"Score calibration: {summary}{cal_note}"


def _summarize_probability_calibration(day_df: pd.DataFrame) -> str:
    prob_col = "hybrid_move_probability" if "hybrid_move_probability" in day_df.columns else "move_probability"
    if prob_col not in day_df.columns or "correct_60m" not in day_df.columns:
        return "Insufficient data for probability calibration."
    pred = _numeric(day_df[prob_col]).mean()
    real = _numeric(day_df["correct_60m"]).mean()
    if pd.isna(pred) or pd.isna(real):
        return "Insufficient data for probability calibration."
    gap = pred - real
    direction = "overconfident" if gap > 0 else "underconfident"
    severity = "significantly" if abs(gap) > 0.15 else "slightly" if abs(gap) > 0.05 else "well"
    if severity == "well":
        return (
            f"Probability well-calibrated: predicted {_pct(pred)} vs realized {_pct(real)} "
            f"(gap: {_r(abs(gap), 3)}). The model's probability estimates closely match observed "
            f"outcomes, indicating reliable uncertainty estimates for signal-quality interpretation."
        )
    cal_note = (
        "The model is overconfident — it systematically overestimates probability quality. "
        "Probability estimates should be deflated or retrained with calibration-aware loss functions."
        if gap > 0
        else "The model is underconfident — it systematically underestimates predictive skill. "
        "Realized hit rates exceed predicted probabilities, so calibration mapping should be updated "
        "(for example isotonic regression or Platt scaling)."
    )
    return (
        f"Predicted {_pct(pred)} vs realized {_pct(real)} — {severity} {direction} "
        f"(gap: {_r(abs(gap), 3)}). {cal_note}"
    )


